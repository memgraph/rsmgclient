#[macro_use]
extern crate maplit;

#[allow(dead_code)]
mod bindings;
mod connection;
mod error;
mod value;

pub use connection::*;
pub use error::*;
pub use value::*;

pub fn add_two(a: i32) -> i32 {
    internal_adder(a, 2)
}

fn internal_adder(a: i32, b: i32) -> i32 {
    a + b
}

